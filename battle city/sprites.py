class Player(pygame.sprite.Sprite):
    def __init__(self, image, pos):
        super().__init__()
        self.image = image
        self.rect = self.image.get_rect()
        self.rect.x = pos[0]
        self.rect.y = pos[1]
        self.speed = 5
        self.dir = 'top'

    def update(self,player_image):
        key = pygame.key.get_pressed()
        if key[pygame.K_a]:
            self.image = pygame.transform.rotate(player_image, 90)
            self.rect.x -= self.speed
            self.dir = 'left'
        elif key[pygame.K_w]:
            self.image = pygame.transform.rotate(player_image, 0)
            self.rect.y -= self.speed
            self.dir = 'top'
        elif key[pygame.K_d]:
            self.image = pygame.transform.rotate(player_image, 270)
            self.rect.x += self.speed
            self.dir = 'right'
        elif key[pygame.K_s]:
            self.image = pygame.transform.rotate(player_image, 180)
            self.rect.y += self.speed
            self.dir = 'down'



